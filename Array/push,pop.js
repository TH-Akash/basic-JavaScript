// push -> last add hbe
let fruits = ["mango", "banana", "apple"];
console.log(fruits);
fruits.push("milk");
console.log(fruits);

// remove  or pop() -> last value remove kore dibe 
let popedFruits = fruits.pop();
console.log(fruits);
console.log("poped fruits is ", popedFruits);

// usshift  stating add korbe value 
fruits.unshift("jackfood")
console.log(fruits);
// remove first element shift -> frist element 
let removeFruits = fruits.shift();
console.log(fruits);
console.log(removeFruits);


